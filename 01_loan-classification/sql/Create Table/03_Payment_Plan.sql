declare @mahdtd nvarchar(20);
declare cur_hdtd	cursor for
		select	MA_HOPDONG_TINDUNG
		from	HOPDONG_TINDUNG;
open	cur_hdtd;
fetch next from cur_hdtd	into @mahdtd
while @@FETCH_STATUS = 0
	begin
		exec	LOP6_B19_TAOKEHOACH	@mahdtd
		fetch	next from cur_hdtd	into @mahdtd
	end
close		cur_hdtd;
deallocate	cur_hdtd;